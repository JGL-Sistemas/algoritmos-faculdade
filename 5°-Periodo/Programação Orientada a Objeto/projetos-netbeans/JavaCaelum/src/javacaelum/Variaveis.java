/*
* Tipos de variaveis
 */
package javacaelum;

/**
 *
 * @author Dieisson Martins
 */
public class Variaveis {

    /**
     * @Imprime variavel
     */
    public static void main(String[] args) {
        int idade;
        idade = 15;

        double pi = 3.14;

        boolean verdade = true;

        char letra = 'D';

        System.out.println("Tipo Inteiro: " + idade);
        System.out.println("Tipo Flutuante: " + pi);
        System.out.println("Tipo Boleano: " + verdade);
        System.out.println("Tipo Char: " + letra);

    }
}
